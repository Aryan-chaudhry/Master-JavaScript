let num1 = 5;
// Binary representation of 5 : 0 1 0 1

let num2 = 7;
// Binary representation of 7 : 0 1 1 1

console.log(num1 & num2);        // 5
console.log(num1 | num2);
console.log(~num1);
console.log(~num2);
console.log(num1 ^ num2);

// left shift;
let a = 2;
console.log(a << 1);


// right shift
let b = 2;
console.log(b >> 1);